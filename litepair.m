function outpair = litepair(inpair)
%saves a lite memory version of inpair.
outpair.delfMHZ = inpair.delfMHZ;
outpair.startj = inpair.startj;
outpair.startka = inpair.startka;
outpair.startkc = inpair.startkc;
outpair.endj = inpair.endj;
outpair.endka = inpair.endka;
outpair.endkc = inpair.endkc;
outpair.shortdescription = inpair.shortdescription;
outpair.labelstring = inpair.labelstring;
end

